unsigned char Gforces(struct Human_Sprite* Stru){

	unsigned char Mod=(Stru->Y+2)%8;
	unsigned char Floor=FALSE;
   
	JJH.P2Coll=Char2SprColl(&JJH,0,1,0);
	
	if((JJH.P2Coll>>4)==1 || (JJH.P2Coll&0xf)==1)Floor=TRUE;
	if((JJH.P2Coll>>4)==7 || (JJH.P2Coll&0xf)==7)Floor=TRUE;
	if(JJH.P2Coll==0x22) Floor=TRUE;		 	
    
    //if (JJH.P1Coll==5)GetKey();
   // if (JJH.P1Coll==4)StageDone=TRUE;
   // if (JJH.P1Coll==6)DelItem(32);
    
    if (!Stru->JumpH){
    	if (Floor&!Mod){
    			Stru->JumpW=0;
    			return 1;
    		}
    	else {
 			++Stru->Y;
 			Stru->Frame=++Stru->Frame&0x01;
 			return 0;
 		}
 	}
}
