unsigned char Char2SprColl(struct Human_Sprite* Stru, signed char Point0,signed char Point1,signed char Offset){//0=NoColl;1=p1Coll;2=p2Coll;3=p1&p2Coll// Two point Collision detection
	register unsigned char p,p0,p1;
	register int CharPos1,CharPos2;
    
 	CharPos1=(((Stru->Y-30-Offset)>>3)*40)+((Stru->X-Stru->ColPoint[Point0])>>3);
    CharPos2=(((Stru->Y-30-Offset)>>3)*40)+((Stru->X-Stru->ColPoint[Point1])>>3);
    p0=*(CharAttrib+CharPos1);
    p1=*(CharAttrib+CharPos2);
    p=p0&0xF0;
    p+=(p1>>4);
    return p;
}
