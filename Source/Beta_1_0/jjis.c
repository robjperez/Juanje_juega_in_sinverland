void SlideSprite(struct Slide_Sprite* Stru, unsigned char Speed, unsigned char Limit, unsigned char Direction,unsigned char Frame,unsigned char Random){
	
	unsigned char rnd=rand();
	if (Random){
		if (rnd>253){ 
			Stru->Value=2;
			Stru->FirstChar=PtrSptr(43);
		}
		if (rnd<2){
			Stru->Value=-2;
			Stru->FirstChar=PtrSptr(45);
		}
	}
	Stru->Rtime=clock()-Stru->Atime;
	if(Stru->Rtime>Speed ){
 		if (Stru->Offset==Limit){
 			Stru->Value=-2;
 			Stru->FirstChar=PtrSptr(45);
 		}
		if (Stru->Offset==0){
			Stru->Value=2;
			Stru->FirstChar=PtrSptr(43);

		}
		Stru->Offset=Stru->Offset+Stru->Value;
 		if(Direction)Stru->X+=Stru->Value;else Stru->Y+=Stru->Value;
 		Stru->Frame=++Stru->Frame&Frame;
 		Stru->Atime=clock();
 	}	
}
