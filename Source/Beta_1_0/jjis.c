void HEvents(signed char value){
	unsigned char SprToChar=Char2SprColl(&JJH,0,1,0);
	
	if((SprToChar>>4)==3)value=2;
	if((SprToChar&0x0f)==3)value=-2;
	
	JJH.Rtime=clock()-JJH.Atime;
 	if(JJH.Rtime>1 ){
 		JJH.Frame=++JJH.Frame&0x07;
  		JJH.X+=value;
 		JJH.CharFloor=Gforces(&JJH);
 		JJH.Atime=clock();
    }	     
}
void upEvents(){
	unsigned char SprToChar=CharSprColl(&JJH,2,10);
	JJH.Rtime=clock()-JJH.Atime;
		if(SprToChar==LADDER){
		JJH.Offset=16;
		JJH.JumpH=0;
		JJH.CharFloor=TRUE;
			if(JJH.Rtime>2){ 
			JJH.Y-=2;
			JJH.Frame=++JJH.Frame&0x01;
			JJH.Atime=clock(); 
		}
	FlagJump=TRUE;
	}
}
void DownEvents(){
	unsigned char SprToChar=CharSprColl(&JJH,2,-8);
	
	JJH.Rtime=clock()-JJH.Atime;
	if(SprToChar==LADDER){
		JJH.Offset=16;
		if(JJH.Rtime>1){
		JJH.Frame=++JJH.Frame&0x01;
		JJH.Y+=2;
		JJH.Atime=clock();
 		}
 		FlagDown=TRUE;
 	}
}
void AirEvents(){
	unsigned char SprToChar=Char2SprColl(&JJH,0,1,0);

	JJH.CharFloor=Gforces(&JJH);
	JJH.Rtime=clock()-JJH.Atime;
	if(JJH.Rtime>1){
		if(JJH.Direction==1){
			if (JJH.JumpH>0){
			JJH.Frame=++JJH.Frame&0x1;
   			JJH.Y-=2;
   			--JJH.JumpH;
			}
		}
       if (JJH.Direction==2){
			if (JJH.JumpH>0){
				JJH.Frame=++JJH.Frame&0x07;
				JJH.Y-=2;
				--JJH.JumpH;
		   	}
		   	if (JJH.X<RLimit&&JJH.JumpW>0){
					if((SprToChar&0x0f)!=3)JJH.X+=2;
					--JJH.JumpW;
			}
		}
        if (JJH.Direction==8){
        	if (JJH.JumpH>0){
				JJH.Frame=++JJH.Frame&0x07;
				JJH.Y-=2;
				--JJH.JumpH;
		   	}
		   	if (JJH.X>LLimit && JJH.JumpW>0){
					if((SprToChar>>4)!=3)JJH.X-=2;
					--JJH.JumpW;
			}
		}
    	JJH.Atime=clock();
    }    
}
void CheckJosytick(){
	
	JUp=CIA1.pra&0x01;
	JDown=(CIA1.pra>>1)&0x01;
	JRight=(CIA1.pra>>3)&0x01;
    JLeft=(CIA1.pra>>2)&0x01;
    
    if(JUp==RELEASE){FlagJump=TRUE;FlagJR=TRUE;FlagJL=TRUE;}
    if(JDown==RELEASE)FlagDown=TRUE;

    
    if (JJH.CharFloor){
       	if(FlagJR && JUp==PUSH && JRight==PUSH){
       		JJH.Direction=2;
       		JJH.CharFloor=FALSE;
    		JJH.JumpH=JumpH;
			JJH.JumpW=24;
			FlagJR=FALSE;
		}
       	if(FlagJL && JUp==PUSH && JLeft==PUSH){
       		JJH.CharFloor=FALSE;
       		JJH.Direction=8;
    		JJH.JumpH=JumpH;
			JJH.JumpW=24;
			FlagJL=FALSE;
        }
    	if(FlagJump && JUp==PUSH && JRight==RELEASE && JLeft==RELEASE){
    		JJH.Direction=1;
    		JJH.CharFloor=FALSE;
    		FlagJump=FALSE;
    		JJH.JumpH=JumpH;
    		upEvents();
        }  
        if(FlagDown && JDown==PUSH){
    		FlagDown=FALSE;
    		DownEvents();
        }
        if(JRight==PUSH){
        	HEvents(2);
        	JJH.Offset=0;
        	if (JJH.X>RLimit)JJH.X=RLimit;
    	}	
    	if(JLeft==PUSH){
    		HEvents(-2);
    		JJH.Offset=8;
    		if (JJH.X<LLimit) JJH.X=LLimit;
    	}


    }else AirEvents();
    
}
